package WeekDayClassTuesday1723;

public class TwoDArrays02 {
    public static void main(String[] args) {
        int[][] matrix = {{10, 20, 30},
                           {44, 55, 66},
                            {100, 339, 440},
        };

        for(int i=0;i<matrix.length;i++){
            for(int j=0;j<matrix[i].length;j++){
                System.out.print(matrix[i] [j]+" ");
            }System.out.println();
        }



    }
}
